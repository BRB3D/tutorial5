public class Application {
    public static void main(String[] args) {

        for(int i = 0; i < 5; i++) {
//            this will print the value of i and also print it in one line
            System.out.printf("The value of i is: %d", i);
        }

        for(int i = 0; i < 3; i++) {
//            this will print the value of i and also print it a new line thanks to the \n
            System.out.printf("The value of i is: %d\n", i);
        }
    }
}
